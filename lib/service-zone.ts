// Calculate the center of the service zone for map positioning
export const getServiceZoneCenter = (): [number, number] => {
  const coords = SERVICE_ZONE_GEOJSON.features[0].geometry.coordinates[0]
  const lats = coords.map((c) => c[1])
  const lngs = coords.map((c) => c[0])
  
  const centerLat = (Math.min(...lats) + Math.max(...lats)) / 2
  const centerLng = (Math.min(...lngs) + Math.max(...lngs)) / 2
  
  return [centerLat, centerLng]
}

// Get bounds of the service zone
export const getServiceZoneBounds = (): [[number, number], [number, number]] => {
  const coords = SERVICE_ZONE_GEOJSON.features[0].geometry.coordinates[0]
  const lats = coords.map((c) => c[1])
  const lngs = coords.map((c) => c[0])
  
  return [
    [Math.min(...lats), Math.min(...lngs)],
    [Math.max(...lats), Math.max(...lngs)],
  ]
}


